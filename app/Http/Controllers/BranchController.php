     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'bsort' => 'required',
            'bfull' => 'required',
        ],[
            'bsort.required' => 'Fill Branch Short Name',
            'bfull.required' => 'Fill Branch Full Name',
        ]);

        Brance::insert([
        'bsort' => $request->bsort,
        'bfull' => $request->bfull,
        ]);

        return back()->with('insert-message','Data Inserted Successfully');
    }

